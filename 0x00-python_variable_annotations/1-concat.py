#!/usr/bin/env python3
"""  type-annotated function concat that takes a string str1 and a
    string str2 as arguments and returns a concatenated string
"""


def concat(str1: str, str2: str) -> str:
    """
    takes a string str1 and a string str2 as
    arguments and returns a concatenated string

    Arguments:
    str1: string
    str2: string

    """
    return str1 + str2
